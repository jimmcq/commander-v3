/**
 * Process Manager — spawns and manages commander instances per tenant.
 * Each tenant gets their own Bun subprocess with isolated config/db/port.
 */

import { spawn, type Subprocess } from "bun";
import type Database from "bun:sqlite";
import { getAllTenants, updateTenantStatus, type Tenant } from "./db";
import { mkdirSync, existsSync, writeFileSync, readFileSync } from "fs";
import { resolve, dirname } from "path";

/** Commander root directory (parent of gateway/) */
const COMMANDER_ROOT = resolve(dirname(import.meta.dir), "..");
const APP_ENTRY = resolve(COMMANDER_ROOT, "src/app.ts");

/** Active commander processes */
const processes = new Map<string, Subprocess>();

